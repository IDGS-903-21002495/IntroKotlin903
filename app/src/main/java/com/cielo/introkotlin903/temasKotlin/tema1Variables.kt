package com.cielo.introkotlin903.temasKotlin

fun main(){
    // Declaración de variables (primera forma)
    // No se puede cambiar el valor (reescribir) cuando declaras una variable con val, es como crear una constante
    val nombre = "Gaby"
    val edad = 35

    println("Hola, mi nombre es $nombre y mi edad es $edad")

    println("Hola mundo!")

    // Declaración de variables (segunda forma usando var)
    // Con var si puede cambiar el valor de la variable
    var numero1 = 10
    var numero2 = 7
    var suma = numero1 + numero2

    println("Suma de $numero1 y $numero2 es $suma")

    print("---------------------------------------------------------------------------------------------------------------")


}